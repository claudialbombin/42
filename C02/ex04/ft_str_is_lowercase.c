#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	u;

	u = 0;
	while (str[u] != '\0')
	{
		if (!(str[u] >= 'a' && str[u] <= 'z'))
			return (0);
		u++;
	}
	return (1);
}
