#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	u;

	u = 0;
	while (str[u] != '\0')
	{
		if (str[u] <= 31 || str[u] == 127)
			return (0);
		u++;
	}
	return (1);
}
