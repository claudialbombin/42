#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	digito;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	digito = (nb % 10) + '0';
	write(1, &digito, 1);
}
