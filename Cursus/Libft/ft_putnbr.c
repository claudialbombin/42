#include <unistd.h>

void	ft_putnbr(int n)
{
	long	nb;
	char	c;

	nb = (long)n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr((int)(nb / 10));
	c = (char)('0' + nb % 10);
	write(1, &c, 1);
}
